<p>Alliances:&nbsp;</p>
<p>You can form alliances with any number of players. The alliance name will be displayed in your profile.</p>
<p>Once you join an alliance, you cannot leave it for 24 real-world hours.&nbsp;</p>
<p>If you create a new alliance, you cannot leave it without disbanding it.</p>
