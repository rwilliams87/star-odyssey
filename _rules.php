<p><br></p>
<p>Resources:&nbsp;</p>
<p>Briterium: Mainly used for defense.&nbsp;</p>
<p>Ridon: Mainly used for science.</p>
<p>Kriden: Mainly used for offense.</p>
<p>Money: Gained via taxes. Used for everything. Only non-military civilians pay taxes.</p>
<p><br></p>
<p><br></p>
<p>The draft:</p>
<p>You can draft your civilians into one of three roles in the military. These roles are perm.</p>
<p>Research: Scientists, the more that work on a project, the faster it gets done.</p>
<p>Military: These are the guys that go into ships that you send off to blow shit up.</p>
<p>Miners: Sucks for these guys. Mine the planet for Briterium / Ridon / Kriden.</p>
<p><br></p>
<p>Note: Military personnel are too busy to fuck. Only civilians make more civilians at a decent rate.</p>
<p>Civilians also keep the money coming in. Try to balance this.&nbsp;</p>
<p><br></p>
<p><br></p>
