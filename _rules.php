<p>Special Ships:</p>
<p>These are manned by scientists and sent off on what&apos;s probably going to be a suicide mission.</p>
<p>They&apos;re fitted with telescopes and stealth tech, not weapons. Cannot attack. Low defense.&nbsp;</p>
<p>The lower # of (any) at-home ship, the less chance of getting caught sending one of these out.</p>
<p>If one is caught, then 1 of 3 things happen:</p>
<p>&nbsp;- The ship is destroyed, and the user is notified that someone was spying on them. (common)</p>
<p>&nbsp;- The ship is captured, and the user is notified WHO was spying on them, and keeps their ship. (uncommon)</p>
<p>&nbsp;- The ship escapes, but the user is notified WHO was spying on them. (very rare)</p>
